    /**
     * 格式化文件大小显示格式
     *
     * @param bytes
     * @param flag
     *            ：true double型结果,精确到小数点后两位;false long型结果,通过移位计算,效率较高
     * @return
     */
    public static String formatSize(long bytes, boolean flag) {
        StringBuilder sb = new StringBuilder();
        if (flag) {
            double gBytes = Math.pow(2, 30);
            double mBytes = Math.pow(2, 20);
            double kBytes = Math.pow(2, 10);
            double formatSize = bytes / gBytes;
            if (formatSize >= 1) {
                sb.append(convert(formatSize)).append("GB");
            } else {
                formatSize = bytes / mBytes;
                if (formatSize >= 1) {
                    sb.append(convert(formatSize)).append("MB");
                } else {
                    formatSize = bytes / kBytes;
                    if (formatSize >= 1) {
                        sb.append(convert(formatSize)).append("KB");
                    } else {
                        sb.append(bytes).append("B");
                    }
                }
            }
        } else {
            long gBytes = (bytes >> 30);
            if (gBytes >= 1) {
                sb.append(gBytes).append("GB");
                return sb.toString();
            }
            long mBytes = (bytes >> 20);
            if (mBytes >= 1) {
                sb.append(mBytes).append("MB");
                return sb.toString();
            }
            long kBytes = (bytes >> 10);
            if (kBytes >= 1) {
                sb.append(kBytes).append("KB");
                return sb.toString();
            }
            sb.append(bytes).append("B");
        }
        return sb.toString();
    }

    private static String convert(double value) {
		/*
		 * long l1 = Math.round(value * 100); // 四舍五入 double ret = l1 / 100.0;
		 * // 注意：使用 100.0 而不是 100 return ret;
		 */
		/*
		 * DecimalFormat df=new java.text.DecimalFormat("#.##"); return
		 * df.format(value);
		 */
        BigDecimal bd = new BigDecimal(value);
        bd = bd.setScale(2, BigDecimal.ROUND_HALF_DOWN);
        return bd.toString();
    }
